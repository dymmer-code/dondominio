defmodule DonDominio do
  @moduledoc """
  DonDominio is a library that interacts with the DonDominio API to register,
  transfer, and manage domains, their contacts and everything related to the
  domain business.

  The actions that can be performed, based on the main modules:

  - `DonDominio.Tool` actions for helping tools.
  - `DonDominio.Account` actions regarding the account info.
  - `DonDominio.Domain` actions for domain registration, renew, transfer, etc.
  - `DonDominio.Contact` actions about contact creation for domains.
  - `DonDominio.Service` actions about hosting and mail.
  - `DonDominio.Certificate` actions to obtain/renew SSL certificates.
  """
end
